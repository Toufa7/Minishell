#include "../minishell.h"

int	spc(char *str)
{
	int	i;
	int	mem;

	i = -1;
	mem = 0;
	while (str[++i])
	{
		if ((str[i] == '<' && str[i + 1] != '<')
			|| (str[i] == '>' && str[i + 1] != '>') || str[i] == '|')
		mem++;
	}
	return (mem);
}
